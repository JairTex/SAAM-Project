    private void jButtonCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastroActionPerformed
        Cliente cliente = new Cliente();
        cliente.setCpf(Integer.parseInt(getjTextFieldCpf().getText()));
        cliente.setNome(getjTextFieldNome().getText());
        cliente.setEmail(getjTextFieldEmail().getText());
        cliente.setPeso(Float.parseFloat(getjTextFieldPeso().getText()));
        cliente.setAltura(Float.parseFloat(getjTextFieldAltura().getText()));
        
        tabelaClientes.adicionarLinha(cliente);
    }//GEN-LAST:event_jButtonCadastroActionPerformed

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        if (jTableCadastro.getSelectedRow() != -1) {
            tabelaClientes.setValueAt(jTextFieldCpf.getText(), jTableCadastro.getSelectedRow(), 0);
            tabelaClientes.setValueAt(jTextFieldNome.getText(), jTableCadastro.getSelectedRow(), 1);
            tabelaClientes.setValueAt(jTextFieldEmail.getText(), jTableCadastro.getSelectedRow(), 2);
            tabelaClientes.setValueAt(jTextFieldPeso.getText(), jTableCadastro.getSelectedRow(), 3);
            tabelaClientes.setValueAt(jTextFieldAltura.getText(), jTableCadastro.getSelectedRow(), 4);
        }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        if (jTableCadastro.getSelectedRow() != -1) {
            tabelaClientes.removerLinha(jTableCadastro.getSelectedRow());
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed

